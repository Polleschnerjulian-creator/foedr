  const euPrograms = programs.filter(p => p.provider.includes("EU"));
  const bundesPrograms = programs.filter(p => !p.provider.includes("EU") && p.targetStates.length === 0);
  const regionalPrograms = programs.filter(p => p.targetStates.length > 0);
  
